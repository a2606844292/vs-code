#<class 'int'>整形
print(type(8))
#<class 'float'>浮点数型
print(type(8.6))
#<class 'bool'>布尔类型
print(type(True))
#<class 'str'>字符串类型
print(type('Hello'))
#整型转换成浮点数型
print(float(8))
#浮点数转换成整型型
print(int(8.0))
#整形转换成布尔类型
print(int(True))
print(float(True))
print(int(False))
print(float(False))
